export function getTranslation(key: string, langCode: string, replaceParams?: Record<string, string | number>): string {
  // Find mapped language
  let currentLang = langCode;
  if (LANGUAGE_FALLBACKS[langCode]) {
    currentLang = LANGUAGE_FALLBACKS[langCode];
  }

  // Fallback to English if dictionary not found
  const dict = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  let text = dict[key] || TRANSLATIONS.en[key] || TRANSLATIONS.ko[key] || key;

  if (replaceParams) {
    Object.entries(replaceParams).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }

  return text;
}
